;Ascii support for Chip16, Shendo 2010
	cls			;Start clean
	spr #0F05		;10x15 px sprites
:start				;Start of the program
	ldi r0,10		;X coordinate
	ldi r1,10		;Y coordinate
	ldi ra,#AAAA		;Aqua color
	ldi r2,string_data_0	;Pointer to the "Hello World" string
	call print_string	;Draw string on screen
	ldi r0,10		;X coordinate
	ldi r1,50		;Y coordinate
	ldi ra,#FFFF		;White color
	ldi r2,string_data_1	;Pointer to the "The quick brown fox" string
	call print_string	;Draw string on screen
	ldi r0,10		;X coordinate
	ldi r1,65		;Y coordinate
	ldi ra,#FFFF		;White color
	ldi r2,string_data_2	;Pointer to the "jumps over the lazy dog" string
	call print_string	;Draw string on screen
:loop
	jmp loop		;Endless loop
:print_string			;Print string on screen
	ldi r3,font_data	;Pointer to the graphic data
	ldm r4,r2		;Get ascii code
	andi r4,255		;Use only low part of the data
	jmz print_end		;If the char is null stop printing
	subi r4,32		;Convert to char offset
	jmc print_end		;If the char is below zero it's unprintable
	muli r4,75		;Convert char offset to byte offset
	add r3,r4		;Get the char sprite address
	call copy_to_cache	;Copy char data to cache
	drw r0,r1,font_cache	;Draw char on screen
	addi r0,8		;Increase X offset
	addi r2,1		;Point to next char
	jmp print_string	;Print next char
:print_end
ret
:copy_to_cache
	ldi r5,38		;Word counter
	ldi r6,font_cache	;Data cache pointer
:copy_loop
	ldm r7,r3		;Copy data to temp buffer
	and r7,ra		;Apply desired color to the sprite
	stm r7,r6		;Store modified data
	addi r3,2		;Go to next word
	addi r6,2		;Point to next word in data cache
	subi r5,1		;Decrease counter
	jmz copy_end		;Desired data has been cached, end process
	jmp copy_loop		;Go to next word
:copy_end
ret
