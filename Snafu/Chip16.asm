;Changelog:
;	Ver 1.1 - 03/24/2011
;	Fixed a bug with player 2 collision detection.
;
;	Ver 1.2 - 04/03/2011
;	Added blinking "Press START button" string.
;
;	Ver 1.3 - 05/08/2011
;	Fixed game for 60 FPS
;
;r0 - Player 1 X coordinate.
;r1 - Player 1 Y coordinate.
;r2 - Player 1 direction (1 - Up, 2 - Down, 4 - Left, 8 - Right).
;r3 - Player 1 score.
;r4 - Player 2 X coordinate.
;r5 - Player 2 Y coordinate.
;r6 - Player 2 moving flags.
;r7 - Player 2 score.
;r8 - Scratchpad.
;r9 - Scratchpad / Collision lookup value.
;ra - String X coordinate / Number of frames to wait.
;rb - String Y coordinate / Value for jme instruction.
;rc - PAD1 status.
;rd - Pointer to the string in memory.
;re - Char sprite memory address.
;rf - Char byte offset / Starting match delay.

	cls				;Start clean
	ldi r8, 45			;Show "Press start" counter value

:start					;Start of the program
	vblnk				;Wait for VBlank
	cls				;Clear the screen
	
	ldi ra,85			;Logo X coordinate
	ldi rb,60			;Logo Y coordinate
	spr #1541			;130x21 px sprite
	drw ra,rb,snafu_logo		;Draw SNAFU logo on screen

	spr #0F05			;10x15 px sprites
	ldi ra,76			;X coordinate
	ldi rb,150			;Y coordinate
	ldi rd,string_data_0		;Pointer to the "Press start" string
	
	jmp start_counter_management	;Skip counter resettings

:reset_start_counter

	ldi r8, 60			;Load default counter value

:start_counter_management

	subi r8, 1			;Decrement counter
	jz reset_start_counter		;Reset counter if it's zero
	mov r9, r8			;Copy counter to scratchpad
	subi r9, 30			;Check if value is greater then 30
	jc skip_press_start		;If the value is less then 15 skip priniting

	call print_string		;Draw string on screen

:skip_press_start			;Skip displaying "Press start" string

	ldi ra,58			;X coordinate
	ldi rb,200			;Y coordinate
	ldi rd,string_data_1		;Pointer to the "Mattel" string
	call print_string		;Draw string on screen
	ldi ra,30			;X coordinate
	ldi rb,215			;Y coordinate
	ldi rd,string_data_2		;Pointer to the "Shendo" string
	call print_string		;Draw string on screen

	ldm rc, #FFF0			;Load PAD1 status
	andi rc, 32			;Isolate START button
	jz start			;The START key is not pressed, draw new frame

:pre_init_game
	ldi r3, 0			;Player 1 default score
	ldi r7, 0			;Player 2 default score

:init_game
	call clear_coll_lookup		;Clear collision lookup table
	vblnk				;Wait for VBlank
	cls				;Clear screen
	bgc #F				;White background
	call draw_score_board		;Draw scoreboard at the bottom of the screen
	call draw_scores		;Draw scores at the bottom of the screen
	spr #1008			;16x16 px sprites
	ldi r0, 0			;Player 1 default X coordinate
	ldi r1, 0			;Player 1 default X coordinate
	ldi r2, 2			;Player 1 moves down by default
	ldi r4, 304			;Player 2 default X coordinate
	ldi r5, 192			;Player 2 default X coordinate
	ldi rf, 15			;Starting match delay

:loop					;Main game loop
	drw r0,r1,player_red_0		;Draw player 1 sprite
	drw r4,r5,player_blue_0		;Draw player 2 sprite
	ldi ra, 7			;Number of frames to wait
	add ra, rf			;Increase delay if this is the first match
	call wait_frames		;Call wait subroutine
	ldi rf, 0			;Clear starting match delay value
	drw r0,r1,player_red_1		;Draw player 1 filler sprite
	drw r4,r5,player_blue_1		;Draw player 2 filler sprite

:check_p2_movement			;Check allowed movement for Player 2
	ldi r6, 0			;Clear Player 2 movement flag
	ldi rb, 1			;Value to use for equal check

:check_p2_up
	ldi r8, 0			;Minimum Y coordinate
	jme r8, r5, check_p2_down	;Minimum Y coordinate has been reached
	mov r9, r4			;Get player 2 X coordinate
	mov r8, r5			;Get player 2 Y coordinate
	subi r8, 16			;Decrease Y coordinate
	call check_coll_table		;Check collision table for collision
	jme r9, rb, check_p2_down	;Collision will happen
	ori r6, 1			;Player 2 can move up

:check_p2_down
	ldi r8, 192			;Maximum Y coordinate
	jme r8, r5, check_p2_left	;Maximum Y coordinate has been reached
	mov r9, r4			;Get player 2 X coordinate
	mov r8, r5			;Get player 2 Y coordinate
	addi r8, 16			;Increase Y coordinate
	call check_coll_table		;Check collision table for collision
	jme r9, rb, check_p2_left	;Collision will happen
	ori r6, 2			;Player 2 can move down

:check_p2_left
	mov r9, r4			;Get player 2 X coordinate
	mov r8, r5			;Get player 2 Y coordinate
	subi r9, 16			;Decrease X coordinate
	jc check_p2_right		;Player 2 can't move left
	call check_coll_table		;Check collision table for collision
	jme r9, rb, check_p2_right	;Collision will happen
	ori r6, 4			;Player 2 can move left

:check_p2_right
	mov r9, r4			;Get player 2 X coordinate
	mov r8, r5			;Get player 2 Y coordinate
	addi r9, 16			;Increase X coordinate
	call check_coll_table		;Check collision table for collision
	jme r9, rb, p2_movement_checks	;Collision will happen
	ori r6, 8			;Player 2 can move down

:p2_movement_checks
	addi r6, 0			;Revaluate flag
	jz p2_collision		;Player 2 can't move in any direction
	rnd r8, 1			;Get random number
	addi r8, 0			;Revaluate flags
	jz move_p2_alt			;Do alternative movement

:move_p2				;Apply movement for Player 2
	call move_p2_up
	call move_p2_down
	call move_p2_left
	call move_p2_right
	jmp store_p2_collision		;Update collision table

:move_p2_alt				;Apply alternate movement for Player 2
	call move_p2_left
	call move_p2_up
	call move_p2_down
	call move_p2_right
	
:store_p2_collision			;Update collision table for Player 2
	mov r9, r4			;Get player 2 X coordinate
	mov r8, r5			;Get player 2 Y coordinate
	call check_coll_table		;Check collision table for collision
	ldi r9, 1			;Collision identifier
	stm r9, r8			;Store identifier to collision lookup

:check_p1_up				;Check if Player 1 should move up
	mov r8,r2			;Copy Player 1 move direction
	andi r8,1			;Isolate the UP movement
	jz check_p1_down		;Player 1 should not move up
	subi r1, 16			;Decrease Player 1 Y coordinate
	jc p1_collision		;Player 1 collided
	andi r2,1			;UP movement confirmed, remove all others

:check_p1_down				;Check if Player 1 should move down
	mov r8,r2			;Copy Player 1 move direction
	andi r8,2			;Isolate the DOWN movement
	jz check_p1_left		;Player 1 should not move down
	ldi r8, 192			;Maximum allowed Y coordinate
	jme r1,r8,p1_collision		;Player 1 collided
	addi r1, 16			;Increase Player 1 Y coordinate
	andi r2,2			;DOWN movement confirmed, remove all others

:check_p1_left				;Check if Player 1 should move left
	mov r8,r2			;Copy Player 1 move direction
	andi r8,4			;Isolate the LEFT movement
	jz check_p1_right		;Player 1 should not move left
	subi r0, 16			;Decrease Player 1 X coordinate
	jc p1_collision		;Player 1 collided
	andi r2,4			;LEFT movement confirmed, remove all others

:check_p1_right				;Check if Player 1 should move right
	mov r8,r2			;Copy Player 1 move direction
	andi r8,8			;Isolate the RIGHT movement
	jz check_p1_collision		;Player 1 should not move right
	ldi r8, 304			;Maximum allowed X coordinate
	jme r0,r8,p1_collision		;Player 1 collided
	addi r0, 16			;Increase Player 1 X coordinate
	andi r2,8			;RIGHT movement confirmed, remove all others

:check_p1_collision			;Check if Player 1 collided with something
	mov r9, r0			;Get player 1 X coordinate
	mov r8, r1			;Get player 1 Y coordinate
	call check_coll_table		;Check collision table for collision
	jz skip_p1_collision		;Collision didn't happen
	jmp p1_collision		;Player 1 collided
	
:skip_p1_collision
	ldi r9, 1			;Collision identifier
	stm r9, r8			;Store identifier to collision lookup
	jmp loop			;Draw new frame

:p1_collision				;Player 1 collided with something
	snd1 70				;Play 500 Hz tone for 70 ms
	ldi ra, 30			;Number of frames to wait
	call wait_frames		;Call wait subroutine
	addi r7, 1			;Add point to Player 2
	ldi r8, 10			;Maximum number of points
	jme r8, r7, p2_won		;Maximum number of points reached
	jmp init_game			;Start new match

:p2_collision				;Player 2 collided with something
	snd1 70				;Play 500 Hz tone for 70 ms
	ldi ra, 30			;Number of frames to wait
	call wait_frames		;Call wait subroutine
	addi r3, 1			;Add point to Player 1
	ldi r8, 10			;Maximum number of points
	jme r8, r3, p1_won		;Maximum number of points reached
	jmp init_game			;Start new match

:p1_won
	spr #0F05			;10x15 px sprites
	vblnk				;Wait for VBlank
	cls				;Clear screen
	ldi ra,122			;X coordinate
	ldi rb,80			;Y coordinate
	ldi rd,string_data_5		;Pointer to the "You won!" string
	call print_string		;Draw string on screen
	ldi ra,82			;X coordinate
	ldi rb,140			;Y coordinate
	ldi rd,string_data_7		;Pointer to the "Restart" string
	call print_string		;Draw string on screen
	jmp wait_a_loop			;Wait for A button

:p2_won
	spr #0F05			;10x15 px sprites
	vblnk				;Wait for VBlank
	cls				;Clear screen
	ldi ra,117			;X coordinate
	ldi rb,80			;Y coordinate
	ldi rd,string_data_6		;Pointer to the "You lost!" string
	call print_string		;Draw string on screen
	ldi ra,82			;X coordinate
	ldi rb,140			;Y coordinate
	ldi rd,string_data_7		;Pointer to the "Restart" string
	call print_string		;Draw string on screen
	jmp wait_a_loop			;Wait for A button

	
:wait_a_loop
	vblnk				;Wait for VBlank
	ldm rc, #FFF0			;Load PAD1 status
	andi rc, 64			;Isolate A button
	jz wait_a_loop			;The A key is not pressed
	jmp pre_init_game		;Restart the match

;SUBROUTINES

:print_string				;Print string on screen
	ldi re,font_data		;Pointer to the graphic data
	ldm rf,rd			;Get ascii code
	andi rf,255			;Use only low part of the data
	jz print_end			;If the char is null stop printing
	subi rf,32			;Convert to char offset
	jc print_end			;If the char is below zero it's unprintable
	muli rf,75			;Convert char offset to byte offset
	add re,rf			;Get the char sprite address
	drw ra,rb,re			;Draw char on screen
	addi ra,8			;Increase X offset
	addi rd,1			;Point to next char
	jmp print_string		;Print next char
:print_end
ret

:wait_frames				;wait for the specified number of frames
	vblnk				;Wait for VBlnk (1 frame)
	ldm rc, #FFF0			;Read PAD1 state
	andi rc, #F			;Isolate DPAD of PAD1
	jz skip_pad_update		;Do not update P1 direction
	mov r2, rc			;Copy PAD1 state to P1 direction
skip_pad_update:
	subi ra, 1			;Decrement counter
	jz wait_end			;If the counter reached zero break subroutine
	jmp wait_frames			;Repeat the process
:wait_end
ret

:clear_coll_lookup			;Clear collision lookup table
	ldi r0, 300			;Number of cells to clean
	ldi r1, 0			;Value to place in cells
	ldi r2, coll_lookup		;Start address of the collision lookup table
:clear_loop
	stm r1, r2			;Place zero at the specified address
	addi r2, 2			;Increase address offset
	subi r0, 1			;Decrement counter
	jz clear_end			;Clearing finished
	jmp clear_loop			;Clear next cell
:clear_end
	ldi r0, 1			;Load a value to place in memory
	ldi r1, coll_lookup		;Pointer to collision lookup table
	stm r0, r1			;Start position of Player 1
	addi r1, 518			;Offset for Player 2 start position
	stm r0, r1			;Start position of Player 2
ret

:draw_score_board			;Draw 320x32 scoreboard at the bottom of the screen
	spr #2008			;16x32 px sprites
	ldi r0, 320			;Sprite X coordinate
	ldi r1, 208			;Sprite Y cordinate
:draw_score_loop
	subi r0, 16			;Decrease X coordinate
	drw r0,r1,block_black		;Draw sprite
	jz draw_score_end		;All parts have been drawn
	jmp draw_score_loop		;Draw next part
:draw_score_end
ret

:draw_scores				;Draw player scores on screen
	spr #0F05			;10x15 px sprites
	ldi ra, 10			;String X coordinate
	ldi rb, 217			;String Y coordinate
	ldi rd,string_data_3		;Pointer to the player 1 score string
	mov r8, r3			;Copy Player 1 score
	addi r8, #30			;Convert to ASCII char
	stm r8, p1_score_string		;Place score in memory
	call print_string		;Print Player 1 score on screen

	ldi ra, 260			;String X coordinate
	ldi rb, 217			;String Y coordinate
	ldi rd,string_data_4		;Pointer to the player 2 score string
	mov r8, r7			;Copy Player 2 score
	addi r8, #30			;Convert to ASCII char
	stm r8, p2_score_string		;Place score in memory
	call print_string		;Print Player 2 score on screen
ret

:check_coll_table			;Check collision table for collisions
	divi r8, 16			;Sprites are 16x16 pixels
	divi r9, 16			;Sprites are 16x16 pixels
	muli r8, 20			;There are 20 fields on X axis
	add r8, r9			;Get the right offset
	muli r8, 2			;Registers are 16 bytes
	addi r8, coll_lookup		;Add collision lookup table memory address
	ldm r9, r8			;Load collision value
	addi r9, 0			;Revaluate flags
ret

; PLAYER 2 MOVEMENT SUBROUTINES

:move_p2_up				;Move Player 2 up
	mov r8, r6			;Copy Player 2 moving flags
	andi r8, 1			;Isolate up flag
	jz move_p2_up_end		;Player 2 musn't move up
	subi r5, 16			;Decrease Y coordinate
	ldi r6, 0			;Disable any further movement
:move_p2_up_end
ret

:move_p2_down				;Move Player 2 down
	mov r8, r6			;Copy Player 2 moving flags
	andi r8, 2			;Isolate down flag
	jz move_p2_down_end		;Player 2 musn't move down
	addi r5, 16			;Increase Y coordinate
	ldi r6, 0			;Disable any further movement
:move_p2_down_end
ret

:move_p2_left				;Move Player 2 left
	mov r8, r6			;Copy Player 2 moving flags
	andi r8, 4			;Isolate left flag
	jz move_p2_left_end		;Player 2 musn't move left
	subi r4, 16			;Decrease X coordinate
	ldi r6, 0			;Disable any further movement
:move_p2_left_end
ret

:move_p2_right				;Move Player 2 right
	mov r8, r6			;Copy Player 2 moving flags
	andi r8, 8			;Isolate right flag
	jz move_p2_right_end		;Player 2 musn't move down
	addi r4, 16			;Increase X coordinate
	ldi r6, 0			;Disable any further movement
:move_p2_right_end
ret

